package application;

public class CharacterMetaData {
	
	public String name; 
	public String sideImage;
	public String mainImage; 
	public int locationX; 
	public int locationY; 
	public int sizeX; 
	public int sizeY; 
	
	public CharacterMetaData(String name, String sideImage, String mainImage, int locationX, int locationY, int sizeX, int sizeY) {
		this.name = name; 
		this.sideImage = sideImage; 
		this.mainImage = mainImage; 
		this.locationX = locationX; 
		this.locationY = locationY; 
		this.sizeX = sizeX; 
		this.sizeY = sizeY; 
		
	}
	

}
